# –ö–æ–¥ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –ü–æ–º–æ—â–Ω–∏–∫ —Å—Ç—É–¥–µ–Ω—Ç–∞
# –ü—Ä–æ–µ–∫—Ç–Ω—ã–π –ø—Ä–∞–∫—Ç–∏–∫—É–º, –≥—Ä—É–ø–ø–∞ 5

# –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –±–∏–±–∏–ª–∏–æ—Ç–µ–∫–∏
import streamlit as st
import pandas as pd
from transformers import pipeline

# –∑–∞–≥—Ä—É–∂–∞–µ–º –º–æ–¥–µ–ª—å "—Å–∞–º–º–∞—Ä–∏" —á–µ—Ä–µ–∑ pipeline
# –¥–µ–∫–æ—Ä–∞—Ç–æ—Ä @st.cache_resource –Ω–µ–æ–±—Ö–æ–¥–∏–º –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –º–æ–¥–µ–ª—å –∑–∞–≥—Ä—É–∂–∞–ª–∞—Å—å –æ–¥–∏–Ω —Ä–∞–∑ –ø—Ä–∏ –ø–µ—Ä–≤–æ–º –ø–æ—Å–µ—â–µ–Ω–∏–∏ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
# –∏ –¥–∞–ª–µ–µ –ø–æ–¥—Ç—è–≥–∏–≤–∞–ª–∞—Å—å –∏–∑ –∫–µ—à–∞
@st.cache_resource
def load_model_summary():  
  # –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–µ –º–æ–¥–µ–ª–∏ —Å–∞–º–º–∞—Ä–∏ –¥–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
  # model = "d0rj/ru-mbart-large-summ"
  # model = 'csebuetnlp/mT5_multilingual_XLSum'
  model = "IlyaGusev/mbart_ru_sum_gazeta"
  return pipeline("summarization", model=model)

# –∑–∞–≥—Ä—É–∂–∞–µ–º –º–æ–¥–µ–ª—å "–≤–æ–ø—Ä–æ—Å-–æ—Ç–≤–µ—Ç" –ø–æ –∫–æ–Ω—Ç–µ–Ω—Ç—É —á–µ—Ä–µ–∑ pipeline
# —Ç–∞–∫–∂–µ –∫–µ—à–∏—Ä—É–µ–º –∑–∞–≥—Ä—É–∑–∫—É –º–æ–¥–µ–ª–∏, –¥–ª—è —ç–∫–æ–Ω–æ–º–∏–∏ —Ä–µ—Å—É—Ä—Å–æ–≤
@st.cache_resource
def load_model_qa():
  return pipeline("question-answering", model="timpal0l/mdeberta-v3-base-squad2")

# —Ñ—É–Ω–∫—Ü–∏—è –æ—á–∏—Å—Ç–∫–∏ —Ç–µ–∫—Å—Ç–∞ –æ—Ç –∑–Ω–∞–∫–∞ –ø–µ—Ä–µ–Ω–æ—Å–∞ –∏ –ª–∏—à–Ω–∏—Ö –ø—Ä–æ–±–µ–ª–æ–≤ –≤ –Ω–∞—á–∞–ª–µ –∏ –∫–æ–Ω—Ü–µ —Å—Ç—Ä–æ–∫–∏
def textcleaner(stcl):
  return stcl.replace("\n","").strip()

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –∑–∞–≥—Ä—É–∑–∫–∏ –º–æ–¥–µ–ª–µ–π
summarizer = load_model_summary()
questioner = load_model_qa()

# –≤—ã–≤–æ–¥–∏–º –ø—Ä–∏–≤–µ—Ä—Å—Ç–≤–µ–Ω–Ω—ã–π —Ç–∞–π—Ç–ª –∏ –∫—Ä–∞—Ç–∫–æ –æ–±–æ–∑–Ω–∞—á–∞–µ–º, —á—Ç–æ –¥–µ–ª–∞–µ—Ç –ø–æ–º–æ—â–Ω–∏–∫
st.title('–ü—Ä–∏–≤–µ—Ç! –Ø –ø–æ–º–æ—â–Ω–∏–∫ —Å—Ç—É–¥–µ–Ω—Ç—É üéà')
st.text('–Ø —É–º–µ—é –¥–µ–ª–∞—Ç—å —Å–∞–º–º–∞—Ä–∏ —Ç–µ–∫—Å—Ç–∞ –∏ –æ—Ç–≤–µ—á–∞—Ç—å –Ω–∞ —Ç–≤–æ–∏ —É—Ç–æ—á–Ω—è—é—â–∏–µ –≤–æ–ø—Ä–æ—Å—ã –∫ —ç—Ç–æ–º—É —Ç–µ–∫—Å—Ç—É')
# st.text('–≠—Ç–æ –ø–æ–∑–≤–æ–ª–∏—Ç —Ç–µ–±–µ –∑–∞ —Å–µ–∫—É–Ω–¥—ã –ø–æ–Ω—è—Ç—å —Å—É—Ç—å –∫–æ–Ω—Ç–µ–Ω—Ç–∞ –∏ –ø–æ–ª—É—á–∏—Ç—å –æ—Ç–≤–µ—Ç—ã –Ω–∞ –≤–æ–ø—Ä–æ—Å—ã')

context_info = st.text_area('–°–∫–æ–ø–∏—Ä—É–π —Ç–µ–∫—Å—Ç –≤ —ç—Ç–æ –ø–æ–ª–µ –≤–≤–æ–¥–∞:', height=300)

# –ø—Ä–µ–¥–ª–∞–≥–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤—ã–±—Ä–∞—Ç—å, —á–µ–º –æ–Ω –±—É–¥–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è
option = st.selectbox(
     '–ë—É–¥–µ–º –¥–µ–ª–∞—Ç—å —Å–∞–º–º–∞—Ä–∏ –∏–ª–∏ –∑–∞–¥–∞–≤–∞—Ç—å –≤–æ–ø—Ä–æ—Å—ã?',
     ('–°–∞–º–º–∞—Ä–∏', '–í–æ–ø—Ä–æ—Å—ã'))

# –ø—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–ª—å–∑–æ–≤—Ç–∞–µ–ª—å—Å–∫–∏–π –≤—ã–±–æ—Ä
if option == '–°–∞–º–º–∞—Ä–∏':
  # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª –°–∞–º–º–∞—Ä–∏, –∑–Ω–∞—á–∏—Ç –≤—ã–¥–∞–µ–º –µ–º—É –∫–Ω–æ–ø–∫—É 
  btn1 = st.button("–ü–æ–µ—Ö–∞–ª–∏!", type="primary")
  if btn1:  
    # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª –∫–Ω–æ–ø–∫—É, –Ω–æ –Ω–∞–º –Ω—É–∂–Ω–æ –ø—Ä–æ–≤–µ—Ä–∏—Ç—å, –Ω–µ "–ø—É—Å—Ç–æ–π" –ª–∏ –∫–æ–Ω—Ç–µ–∫—Å—Ç, –ø–æ –∫–æ—Ç–æ—Ä–æ–º—É –º—ã —Å–æ–±–∏—Ä–∞–µ–º —Å–¥–µ–ª–∞—Ç—å —Å–∞–º–º–∞—Ä–∏
    if len(context_info) > 99:
      # –∑–∞–ø—É—Å–∫–∞–º –º–æ–¥–µ–ª—å —Å–∞–º–º–∞—Ä–∏ –ø–æ –≤–≤–µ–¥–µ–Ω–Ω–æ–º—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º —Ç–µ–∫—Å—Ç—É
      result = summarizer(context_info, truncation=True)
      st.write(result[0]['summary_text'])
    else:
      # —Å–ª–∏—à–∫–æ–º –º–∞–ª–æ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞, –ø–æ—ç—Ç–æ–º—É –≤—ã–¥–∞–µ–º –µ–º—É –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ
      st.write('–¢–µ–∫—Å—Ç –¥–æ–ª–∂–µ–Ω —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ö–æ—Ç—è –±—ã 100 —Å–∏–º–≤–æ–ª–æ–≤')
else:
  # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª "–í–æ–ø—Ä–æ—Å—ã"
  # –≤—ã–≤–æ–¥–∏–º –ø–æ–ª–µ –≤–≤–æ–¥—è –¥–ª—è –≤–æ–ø—Ä–æ—Å–∞ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
  quest = st.text_input('–ù–∞–ø–∏—à–∏ —Å–≤–æ–π –≤–æ–ø—Ä–æ—Å –∫ —Ç–µ–∫—Å—Ç—É: ')
  # –≤—ã–≤–æ–¥–∏–º –∫–Ω–æ–ø–∫—É –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –º–æ–¥–µ–ª–∏ 
  btn2 = st.button("–ù–∞–π–¥–∏ –æ—Ç–≤–µ—Ç", type="primary")
  if btn2:
    # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª –∫–Ω–æ–ø–∫—É, –∑–∞–ø—É—Å–∫–∞–µ–º –º–æ–¥–µ–ª—å QA    
    result = questioner(question = quest, context = context_info)
    # –ø—Ä–æ–≤–µ—Ä—è–µ–º —Å–∫–æ—Ä –ø–æ–ª—É—á–µ–Ω–Ω–æ–≥–æ –æ—Ç –º–æ–¥–µ–ª–∏ –æ—Ç–≤–µ—Ç–∞. 
    if result['score'] > 0.01:
      # —Å–∫–æ—Ä –æ–∫–∞–∑–∞–ª—Å—è –ø—Ä–∏–µ–º–ª–µ–º—ã, –≤—ã–≤–æ–¥–∏–º –æ—Ç–≤–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é      
      st.write(textcleaner(result['answer']))
      # –∏ –æ—Ç—Ä–∞–∂–∞–µ–º —Å–∫–æ—Ä, —á—Ç–æ–±—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø–æ–Ω–∏–º–∞–ª –æ—Ü–µ–Ω–æ—á–Ω—É—é –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω–æ—Å—Ç—å –æ—Ç–≤–µ—Ç–∞
      st.write('score is: ', result['score'])
    else:
      # —Å–∫–æ—Ä –æ–∫–∞–∑–∞–ª—Å—è –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º–æ –Ω–∏–∑–∫–∏–º –∏ –º—ã –ø—Ä–æ—Å—Ç–æ —Å–æ–æ–±—â–∞–µ–º –ø–æ–ª—å–∑–æ–≤—Ç–∞–µ–ª—é, —á—Ç–æ –æ—Ç–≤–µ—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω
      st.write('–Ω–µ –ø–æ–ª—É—á–∞–µ—Ç—Å—è –Ω–∞–π—Ç–∏ –≤ —Ç–µ–∫—Å—Ç–µ –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω—ã–π –æ—Ç–≤–µ—Ç')