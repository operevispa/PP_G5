"""
–û—Å–Ω–æ–≤–Ω–æ–π –∫–æ–¥ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –ü–æ–º–æ—â–Ω–∏–∫ —Å—Ç—É–¥–µ–Ω—Ç–∞
–ü—Ä–æ–µ–∫—Ç–Ω—ã–π –ø—Ä–∞–∫—Ç–∏–∫—É–º, –≥—Ä—É–ø–ø–∞ 5

–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –≤ —Å–µ–±–µ –Ω–∞–±–æ—Ä —Ä–∞–∑–Ω—ã—Ö –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –ø–æ–º–æ–≥–∞—é—Ç –≤ —Ç–∏–ø–æ–≤—ã—Ö
–∑–∞–¥–∞—á–∞—Ö —É—á–∞—â–∏—Ö—Å—è –∏ –≤ —Å–æ–≤–æ–∫—É–ø–Ω–æ—Å—Ç–∏ –æ–±—Ä–∞–∑—É—é—Ç "—Ä–∞–±–æ—á–µ–µ –º–µ—Å—Ç–æ" —Å—Ç—É–¥–µ–Ω—Ç–∞. –ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ
–∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –Ω–∞ WEB-—Å–µ—Ä–≤–µ—Ä–µ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ Strealit –∏ –ø—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ—Ç
—Ä–∞–±–æ—Ç—É —á–µ—Ä–µ–∑ –ª—é–±–æ–π, —É–¥–æ–±–Ω—ã–π –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –±—Ä–∞—É–∑–µ—Ä.

–ù–∞ —Ç–µ–∫—É—â–∏–π –º–æ–º–µ–Ω—Ç —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω —Å–ª–µ–¥—É—é—â–∏–π —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª:
1. –ü–æ–¥–≥–æ—Ç–æ–≤–∫–∞ –∫—Ä–∞—Ç–∫–æ–≥–æ —Å–æ–¥–µ—Ä–∂–∞–Ω–∏—è –∏–∑ –±–æ–ª—å—à–æ–≥–æ —Ç–µ–∫—Å—Ç–∞.
2. –ü–æ–∏—Å–∫ –æ—Ç–≤–µ—Ç–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ –ø–æ –∑–∞–¥–∞–Ω–Ω–æ–º—É –≤–æ–ø—Ä–æ—Å—É.
"""

# –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –±–∏–±–∏–ª–∏–æ—Ç–µ–∫–∏
import streamlit as st

from transformers import pipeline


@st.cache_resource
def load_model_summary():
    """
    –§—É–Ω–∫—Ü–∏—è –∑–∞–≥—Ä—É–∂–∞–µ—Ç –º–æ–¥–µ–ª—å "—Å–∞–º–º–∞—Ä–∏" —á–µ—Ä–µ–∑ pipeline.
    –î–µ–∫–æ—Ä–∞—Ç–æ—Ä @st.cache_resource –Ω–µ–æ–±—Ö–æ–¥–∏–º –¥–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –º–æ–¥–µ–ª—å –∑–∞–≥—Ä—É–∂–∞–ª–∞—Å—å
    –æ–¥–∏–Ω —Ä–∞–∑ –ø—Ä–∏ –ø–µ—Ä–≤–æ–º –ø–æ—Å–µ—â–µ–Ω–∏–∏ —Å—Ç—Ä–∞–Ω–∏—Ü—ã –∏ –¥–∞–ª–µ–µ –ø–æ–¥—Ç—è–≥–∏–≤–∞–ª–∞—Å—å –∏–∑ –∫–µ—à–∞.

    –í–æ–∑–º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã—Ö –º–æ–¥–µ–ª–µ–π:
    'csebuetnlp/mT5_multilingual_XLSum'
    'd0rj/ru-mbart-large-summ'
    'IlyaGusev/mbart_ru_sum_gazeta'
    """

    model = 'IlyaGusev/mbart_ru_sum_gazeta'
    return pipeline('summarization', model=model)


@st.cache_resource
def load_model_qa():
    """
    –§—É–Ω–∫—Ü–∏—è –∑–∞–≥—Ä—É–∂–∞–µ—Ç –º–æ–¥–µ–ª—å "–≤–æ–ø—Ä–æ—Å-–æ—Ç–≤–µ—Ç" –ø–æ –∫–æ–Ω—Ç–µ–Ω—Ç—É
    —Ç–∞–∫–∂–µ –∫–µ—à–∏—Ä—É–µ–º –∑–∞–≥—Ä—É–∑–∫—É –º–æ–¥–µ–ª–∏, –¥–ª—è —ç–∫–æ–Ω–æ–º–∏–∏ —Ä–µ—Å—É—Ä—Å–æ–≤ —á–µ—Ä–µ–∑ –¥–µ–∫–æ—Ä–∞—Ç–æ—Ä @st.cache_resource
    """

    return pipeline("question-answering", model="timpal0l/mdeberta-v3-base-squad2")


def get_sum(context, st=None):
    """
    –§—É–Ω–∫—Ü–∏—è –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫—Ä–∞—Ç–∫–æ–µ —Å–æ–¥–µ—Ä–∂–∞–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞. –ï—Å–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è st == None,
    —Ç–æ –Ω–µ –≤—ã–≤–æ–¥–∏—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Ä–∞–±–æ—Ç—ã –≤ –≤–∏–¥–∂–µ—Ç. –¢–∞–∫–æ–π —Ä–µ–∂–∏–º –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è 
    –ø—Ä–æ–≥—Ä–∞–º–º–Ω–æ–≥–æ –ø–æ–ª—É—á–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤, –≤ —á–∞—Å—Ç–Ω–æ—Å—Ç–∏ –ø—Ä–∏ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è.

    Args:
        context (str): –¢–µ–∫—Å—Ç, –∫–æ—Ç–æ—Ä—ã–π –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ —Å–æ–∫—Ä–∞—Ç–∏—Ç—å. –î–ª–∏–Ω–∞ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –±–æ–ª–µ–µ 100 —Å–∏–º–≤–æ–ª–æ–≤.
        st (module): –ú–æ–¥—É–ª—å  Streamlit, —á–µ—Ä–µ–∑ –∫–æ—Ç–æ—Ä—ã–π —Å—Ç—Ä–æ–∏–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å. –î–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è None.

    Returns:
        str: –ö—Ä–∞—Ç–∫–∏–π —Ç–µ–∫—Å—Ç.
    """

    if len(context) > 99:
        # –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –∑–∞–≥—Ä—É–∑–∫–∏ –º–æ–¥–µ–ª–∏ —Å–∞–º–º–∞—Ä–∏
        summarizer = load_model_summary()

        # –∑–∞–ø—É—Å–∫–∞–º –º–æ–¥–µ–ª—å —Å–∞–º–º–∞—Ä–∏ –ø–æ –≤–≤–µ–¥–µ–Ω–Ω–æ–º—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º —Ç–µ–∫—Å—Ç—É
        result = summarizer(context)[0]['summary_text']
    else:
        # —Å–ª–∏—à–∫–æ–º –º–∞–ª–æ –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞, –ø–æ—ç—Ç–æ–º—É –≤—ã–¥–∞–µ–º –µ–º—É –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ
        result = '–¢–µ–∫—Å—Ç –¥–æ–ª–∂–µ–Ω —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ö–æ—Ç—è –±—ã 100 —Å–∏–º–≤–æ–ª–æ–≤'

    if not st is None:
        st.write(result)

    return result


def get_answer(context, question, st=None):
    """ 
    –í—ã–≤–æ–¥–∏—Ç –Ω–∞–π–¥–µ–Ω–Ω—ã–π –æ—Ç–≤–µ—Ç –≤ –∑–∞–¥–∞–Ω–Ω–æ–º —Ç–µ–∫—Å—Ç–µ –ø–æ –∑–∞–¥–∞–Ω–Ω–æ–º—É –≤–æ–ø—Ä–æ—Å—É. –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ª–æ–≤–∞—Ä—å
    —Å –Ω–∞–π–¥–µ–Ω–Ω—ã–º –æ—Ç–≤–µ—Ç–æ–º –∏ –æ—Ü–µ–Ω–∫–æ–π. –ï—Å–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è st == None, —Ç–æ –Ω–µ –≤—ã–≤–æ–¥–∏—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    —Ä–∞–±–æ—Ç—ã –≤ –≤–∏–¥–∂–µ—Ç. –¢–∞–∫–æ–π —Ä–µ–∂–∏–º –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –ø—Ä–æ–≥—Ä–∞–º–º–Ω–æ–≥–æ –ø–æ–ª—É—á–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤,
    –≤ —á–∞—Å—Ç–Ω–æ—Å—Ç–∏ –ø—Ä–∏ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è.        

    Args:        
        context (str): –¢–µ–∫—Å—Ç, –≤ –∫–æ—Ç–æ—Ä–æ–º –∏—â–µ–º –æ—Ç–≤–µ—Ç—ã.
        question (str): –í–æ–ø—Ä–æ—Å, –æ—Ç–≤–µ—Ç –Ω–∞ –∫–æ—Ç–æ—Ä—ã–π —Ö–æ—Ç–∏–º –ø–æ–ª—É—á–∏—Ç—å.
        st (module): –ú–æ–¥—É–ª—å  Streamlit, —á–µ—Ä–µ–∑ –∫–æ—Ç–æ—Ä—ã–π —Å—Ç—Ä–æ–∏–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å. –î–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è None.

    Returns:
        dict: answer (str): –ù–∞–π–¥–µ–Ω–Ω—ã–π –æ—Ç–≤–µ—Ç.
              score (float): –û—Ü–µ–Ω–∫–∞ –≤–µ—Ä–Ω–æ—Å—Ç—å –æ—Ç–≤–µ—Ç–∞.
    """

    questioner = load_model_qa()

    result = questioner(question=question, context=context)
    result['answer'] = result['answer'].strip(' ".<>:,=*\n')

    # –ï—Å–ª–∏ st –Ω–µ –∑–∞–¥–∞–Ω–æ, —Ç–æ –≤—ã–≤–æ–¥–∏—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –≤ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å –Ω–µ –Ω—É–∂–Ω–æ
    if not st is None:
        # –ø—Ä–æ–≤–µ—Ä—è–µ–º —Å–∫–æ—Ä –ø–æ–ª—É—á–µ–Ω–Ω–æ–≥–æ –æ—Ç –º–æ–¥–µ–ª–∏ –æ—Ç–≤–µ—Ç–∞.
        if result['score'] > 0.01:
            # –û—Ü–µ–Ω–∫–∞ –æ–∫–∞–∑–∞–ª–∞—Å—å –ø—Ä–∏–µ–º–ª–µ–º–æ–π, –≤—ã–≤–æ–¥–∏–º –æ—Ç–≤–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é.
            st.write(result['answer'])
            # –ò –æ—Ç—Ä–∞–∂–∞–µ–º –æ—Ü–µ–Ω–∫—É, —á—Ç–æ–±—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø–æ–Ω–∏–º–∞–ª –æ—Ü–µ–Ω–æ—á–Ω—É—é –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω–æ—Å—Ç—å –æ—Ç–≤–µ—Ç–∞.
            st.write('score is: ', result['score'])
        else:
            # –û—Ü–µ–Ω–∫–∞ –æ–∫–∞–∑–∞–ª–∞—Å—å –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º–æ –Ω–∏–∑–∫–æ–π –∏ –º—ã –ø—Ä–æ—Å—Ç–æ —Å–æ–æ–±—â–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é, —á—Ç–æ –æ—Ç–≤–µ—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω.
            st.write('–Ω–µ –ø–æ–ª—É—á–∞–µ—Ç—Å—è –Ω–∞–π—Ç–∏ –≤ —Ç–µ–∫—Å—Ç–µ –¥–æ—Å—Ç–æ–≤–µ—Ä–Ω—ã–π –æ—Ç–≤–µ—Ç')

    return result


# –î–µ–ª–∞–µ–º —É—Å–ª–æ–≤–∏–µ, —á—Ç–æ–±—ã –∫–æ–¥ –Ω–µ –æ—Ç—Ä–∞–±–∞—Ç—ã–≤–∞–ª—Å—è –≤–æ –≤—Ä–µ–º—è –∏–º–ø–æ—Ä—Ç–∞ –≤ –¥—Ä—É–≥–∏–µ –º–æ–¥—É–ª–∏, –≤ —á–∞—Å—Ç–Ω–æ—Å—Ç–∏ –¥–ª—è —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è.
if __name__ == '__main__':
    # –≤—ã–≤–æ–¥–∏–º –ø—Ä–∏–≤–µ—Ä—Å—Ç–≤–µ–Ω–Ω—ã–π —Ç–∞–π—Ç–ª –∏ –∫—Ä–∞—Ç–∫–æ –æ–±–æ–∑–Ω–∞—á–∞–µ–º, —á—Ç–æ –¥–µ–ª–∞–µ—Ç –ø–æ–º–æ—â–Ω–∏–∫
    st.title('–ü—Ä–∏–≤–µ—Ç! –Ø –ø–æ–º–æ—â–Ω–∏–∫ —Å—Ç—É–¥–µ–Ω—Ç—É üéà')
    st.text('–Ø —É–º–µ—é —Å–æ–∫—Ä–∞—â–∞—Ç—å —Ç–µ–∫—Å—Ç –∏–ª–∏ –∏—Å–∫–∞—Ç—å –æ—Ç–≤–µ—Ç—ã –≤ –Ω–µ–º')

    context_info = st.text_area(
        '–°–∫–æ–ø–∏—Ä—É–π —Ç–µ–∫—Å—Ç –≤ —ç—Ç–æ –ø–æ–ª–µ –≤–≤–æ–¥–∞:', key='context', height=300, max_chars=2000)

    # –ø—Ä–µ–¥–ª–∞–≥–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤—ã–±—Ä–∞—Ç—å, —á–µ–º –æ–Ω –±—É–¥–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è
    option = st.selectbox(
        '–ë—É–¥–µ–º —Å–æ–∫—Ä–∞—â–∞—Ç—å —Ç–µ–∫—Å—Ç –∏–ª–∏ –∏—Å–∫–∞—Ç—å –≤ –Ω–µ–º –æ—Ç–≤–µ—Ç—ã?',
        ('–ò—Å–∫–∞—Ç—å –æ—Ç–≤–µ—Ç—ã', '–°–æ–∫—Ä–∞—â–∞—Ç—å'), key='option')

    # –ø—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–ª—å–∑–æ–≤—Ç–∞–µ–ª—å—Å–∫–∏–π –≤—ã–±–æ—Ä
    if option == '–°–æ–∫—Ä–∞—â–∞—Ç—å':
        # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª –°–æ–∫—Ä–∞—â–∞—Ç—å, –∑–Ω–∞—á–∏—Ç –≤—ã–¥–∞–µ–º –µ–º—É –∫–Ω–æ–ø–∫—É
        btn1 = st.button("–ü–æ–µ—Ö–∞–ª–∏!", key="summ", type="primary")
        if btn1:
            # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª –∫–Ω–æ–ø–∫—É, –Ω–æ –Ω–∞–º –Ω—É–∂–Ω–æ –ø—Ä–æ–≤–µ—Ä–∏—Ç—å, –Ω–µ "–ø—É—Å—Ç–æ–π" –ª–∏ –∫–æ–Ω—Ç–µ–∫—Å—Ç, –ø–æ –∫–æ—Ç–æ—Ä–æ–º—É –º—ã —Å–æ–±–∏—Ä–∞–µ–º —Å–¥–µ–ª–∞—Ç—å —Å–∞–º–º–∞—Ä–∏
            get_sum(context_info, st)
    else:
        # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª "–ò—Å–∫–∞—Ç—å –æ—Ç–≤–µ—Ç—ã"
        # –≤—ã–≤–æ–¥–∏–º –ø–æ–ª–µ –≤–≤–æ–¥—è –¥–ª—è –≤–æ–ø—Ä–æ—Å–∞ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        quest = st.text_input('–ù–∞–ø–∏—à–∏ —Å–≤–æ–π –≤–æ–ø—Ä–æ—Å –∫ —Ç–µ–∫—Å—Ç—É: ', max_chars=200)
        # –≤—ã–≤–æ–¥–∏–º –∫–Ω–æ–ø–∫—É –¥–ª—è –∑–∞–ø—É—Å–∫–∞ –º–æ–¥–µ–ª–∏
        btn2 = st.button("–ù–∞–π–¥–∏ –æ—Ç–≤–µ—Ç", key="button", type="primary")
        if btn2:
            # –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª –∫–Ω–æ–ø–∫—É, –∑–∞–ø—É—Å–∫–∞–µ–º –º–æ–¥–µ–ª—å QA
            get_answer(context_info, quest, st)
